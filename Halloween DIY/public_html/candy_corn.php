<h3>Instructions:</h3>
<ol>
	<li> Fill the two bowls with one cup water each</li>
	<li> Add 8 drops of yellow food coloring to one bowl, and 8 drops of orange to the other bowl. Stir until evenly distrubuted.</li>
	<li> Place coffee filters in bowls and then let dry overnight.</li>
	<li> Once dry, scrunch bottom of coffee filters and hot glue it to the cardboard.</li>
	<li> Glue in desired shape and then cut excess cardboard for your very own candy corn decoration.</li>
</ol>
